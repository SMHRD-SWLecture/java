package ex03증감비교논리연산자;

public class Ex01증감연산자 {

	public static void main(String[] args) {
		// 증감연산자(++ --) : 변수에 저장된 값을 1씩 증가 or 감소
		
		int i = 3;
		// 증감연산자는 연산자의 위치에 따라서 연산의 결과값이 달라짐
		// i++ -> 해당 줄의 코드를 실행하고 나서 1 증가
		// ++i -> 해당 줄의 코드를 실행하면서 1 증가
		
		System.out.println(i++); 
		System.out.println(i);
		System.out.println(++i);
		System.out.println(i);
		
		
		
		
		
		

	}

}
